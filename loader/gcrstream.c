void print_bit(int p, int bit1, int bit2) {
    if (bit1 == bit2) printf("%d", bit1);
    else printf(".");
    if ((p % 40) == 0) printf ("\n");
    else if ((p % 8) == 0) printf(" ");
}

void decode(int* data1, int* data2) {
    int b, i, p;
    int gcr[16] = {
        0b00001010,     //0f    0
        0b00001011,     //07    1
        0b00010010,     //0d    2
        0b00010011,     //05    3
        0b00001110,     //0b    4
        0b00001111,     //03    5
        0b00010110,     //09    6
        0b00010111,     //01    7
        0b00001001,     //0e    8
        0b00011001,     //06    9
        0b00011010,     //0c    a
        0b00011011,     //04    b
        0b00001101,     //0a    c
        0b00011101,     //02    d
        0b00011110,     //08    e
        0b00010101      //00    f
    };
    p = 0;
    for (i = 0; i < 256; i++) {
        for (b = 4; b >= 0; b--) {
            p++;
            print_bit(p, (gcr[data1[i] >> 4] >> b) & 1, (gcr[data2[i] >> 4] >> b) & 1);
        }
        for (b = 4; b >= 0; b--) {
            p++;
            print_bit(p, (gcr[data1[i] & 0xf] >> b) & 1, (gcr[data2[i] & 0xf] >> b) & 1);
        }
    }
}

